<script>
    function toggleFooterMenu(menuId, iconId) {
        // Cek apakah layar saat ini adalah desktop (Tailwind 'md' breakpoint biasanya 768px ke atas)
        // Jika desktop, hentikan fungsi karena menu tidak boleh dilipat
        if (window.innerWidth >= 768) return;

        const menu = document.getElementById(menuId);
        const icon = document.getElementById(iconId);

        // Toggle class 'hidden' pada menu dan putar ikon panah
        if (menu.classList.contains('hidden')) {
            menu.classList.remove('hidden');
            icon.classList.add('rotate-180'); // Memutar panah ke atas
        } else {
            menu.classList.add('hidden');
            icon.classList.remove('rotate-180'); // Mengembalikan panah ke bawah
        }
    }
</script>